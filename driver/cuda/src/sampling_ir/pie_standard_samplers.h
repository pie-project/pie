// A baked program: the v4 bytecode + its attach manifest, ready for
// `lower_bytecode_v4(bytecode, len, manifest, opts)` / `get_or_compile`.
struct StandardSamplerProgram {
    const std::uint8_t* bytecode = nullptr;
    std::size_t len = 0;
    ProgramManifest manifest;   // one InputBind per slot, slot order
    bool valid = false;
};

// Look up the baked program for `kind` at vocab `vocab`. Returns `valid=false`
// for a vocab without a baked entry (use the generator path). Since #25 ALL six
// standard kinds are k-invariant (top-k `k` rides a host-submit value-id), so
// each has ONE canonical bytecode → all are baked + exact-hash recognized.
inline StandardSamplerProgram standard_sampler_program(StandardSamplerKind kind,
                                                       std::uint32_t vocab) {
    StandardSamplerProgram p;
    const InputBind logits{BindKind::Logits, 0, HostAvailability::SubmitBound};
    const InputBind tparam{BindKind::HostTensor, 0, HostAvailability::SubmitBound};  // T (key 0)
    const InputBind pparam{BindKind::HostTensor, 1, HostAvailability::SubmitBound};  // filter @key1 (min_p OR top_p)
    const InputBind kparam1{BindKind::HostTensor, 1, HostAvailability::SubmitBound}; // k @key1 (TopK: [Logits,T,k])
    const InputBind kparam2{BindKind::HostTensor, 2, HostAvailability::SubmitBound}; // k @key2 (TopKTopP: [Logits,T,top_p,k])
    if (vocab != 151936u) return p;  // only the qwen3 V is baked here; else use the generator
    switch (kind) {
        case StandardSamplerKind::Argmax:
            p.bytecode = kStdArgmax151936; p.len = sizeof(kStdArgmax151936);
            p.manifest = {logits}; p.valid = true; break;
        case StandardSamplerKind::Temperature:
            p.bytecode = kStdTemp151936; p.len = sizeof(kStdTemp151936);
            p.manifest = {logits, tparam}; p.valid = true; break;
        case StandardSamplerKind::MinP:
            p.bytecode = kStdMinp151936; p.len = sizeof(kStdMinp151936);
            p.manifest = {logits, tparam, pparam}; p.valid = true; break;
        case StandardSamplerKind::TopP:
            p.bytecode = kStdTopP151936; p.len = sizeof(kStdTopP151936);
            p.manifest = {logits, tparam, pparam}; p.valid = true; break;  // pparam@key1 = top_p
        case StandardSamplerKind::TopK:  // #25: T + k both host-submit ([Logits,T,k])
            p.bytecode = kStdTopK151936; p.len = sizeof(kStdTopK151936);
            p.manifest = {logits, tparam, kparam1}; p.valid = true; break;
        case StandardSamplerKind::TopKTopP:  // #25: T + top_p + k host-submit ([Logits,T,top_p,k])
            p.bytecode = kStdTopKTopP151936; p.len = sizeof(kStdTopKTopP151936);
            p.manifest = {logits, tparam, pparam, kparam2}; p.valid = true; break;
    }
    return p;
}

