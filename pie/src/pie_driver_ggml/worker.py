def _write_startup_toml(
    *,
    driver_cfg: GgmlDriverConfig,
    snapshot_dir: str,
    aux_socket_path: str,
    out_path: Path,
) -> None:
    """Emit the binary's startup config. Format matches `driver/ggml/dev.toml`."""
    lines = [
        "# Auto-generated by pie_driver_ggml; do not edit.",
        "",
        "[shmem]",
        # Must match runtime/src/device.rs SHMEM_* constants — resp_buf is
        # 4 MiB on the runtime side, not 1 MiB; mismatched sizes silently
        # break the channel and the runtime sends nothing.
        'name = "/pie_shmem"',
        "num_slots = 8",
        "req_buf = 4194304",
        "resp_buf = 4194304",
        "spin_us = 0",
        "",
        "[model]",
        f'hf_path = "{snapshot_dir}"',
        f"n_gpu_layers = {driver_cfg.n_gpu_layers}",
        f"n_ctx = {driver_cfg.n_ctx}",
        "",
        "[batching]",
        f"kv_page_size = {driver_cfg.kv_page_size}",
        f"max_num_kv_pages = {driver_cfg.max_num_kv_pages}",
        f"max_batch_tokens = {driver_cfg.max_batch_tokens}",
        f"max_batch_size = {driver_cfg.max_batch_size}",
        f"cpu_pages = {driver_cfg.cpu_pages}",
        "",
        "[aux_ipc]",
        f'socket_path = "{aux_socket_path}"',
        "",
    ]
    out_path.write_text("\n".join(lines))


# =============================================================================
# Aux IPC client — marshal copy_* RPCs into binary commands
# =============================================================================
#
# Wire format mirrors `driver/ggml/src/aux_ipc.hpp`:
#   header (24 bytes, LE):
#     u32 magic = 0x41454950 ('PIEA')
#     u32 method (1=d2h, 2=h2d, 3=d2d, 4=h2h)
#     u32 n_pages
#     u32 reserved
#     u64 req_id
#   payload: n_pages × { u32 src; u32 dst; }
#   ack (16 bytes, LE):
#     u32 magic = 0x4B414950 ('PIAK')
#     u32 status
#     u64 req_id


_AUX_MAGIC      = 0x41454950
_AUX_ACK_MAGIC  = 0x4B414950
_AUX_HDR        = struct.Struct("<IIIIQ")    # 24 bytes (magic, method, n_pages, reserved, req_id)
_AUX_PAIR       = struct.Struct("<II")       # 8 bytes
_AUX_ACK        = struct.Struct("<IIQ")      # 16 bytes (magic, status, req_id)
_AUX_LOAD_HDR   = struct.Struct("<QII")      # 16 bytes (adapter_id, path_len, reserved)
_METHOD_LOAD_ADAPTER = 5


class _AuxClient:
    """Synchronous unix-socket client for the binary's aux IPC server.
    Single-threaded — guarded by a lock since the wrapper's RPC loop may
    dispatch handlers concurrently in principle."""

    def __init__(self, socket_path: str):
        self._path = socket_path
        self._sock = None
        self._lock = threading.Lock()
        self._next_req = 1

    def call(self, method: int, pairs: list[tuple[int, int]]) -> None:
        payload = b"".join(_AUX_PAIR.pack(s, d) for (s, d) in pairs)
        self._send_command(method, payload, n_pages=len(pairs))

    def load_adapter(self, adapter_id: int, file_path: str) -> None:
        """Send a LoadAdapter command pointing at an on-disk safetensors
        file. The binary reads + parses the file and registers the
        adapter under `adapter_id`."""
        path_bytes = file_path.encode("utf-8")
        sub = _AUX_LOAD_HDR.pack(adapter_id, len(path_bytes), 0)
        self._send_command(_METHOD_LOAD_ADAPTER, sub + path_bytes, n_pages=0)

    def close(self) -> None:
        with self._lock:
            if self._sock is not None:
                try:
                    self._sock.close()
                except Exception:
                    pass
                self._sock = None

    # -------------------------------------------------------------------------
    # Internals
    # -------------------------------------------------------------------------

    def _send_command(self, method: int, payload: bytes, n_pages: int) -> None:
        """Send one aux command and read its ack. Holds `self._lock` for the
        full round-trip so concurrent callers serialize cleanly."""
        with self._lock:
            self._connect()
            req_id = self._next_req
            self._next_req += 1
            hdr = _AUX_HDR.pack(_AUX_MAGIC, method, n_pages, 0, req_id)
            self._sock.sendall(hdr + payload)
            self._read_ack(req_id)

    def _connect(self) -> None:
        if self._sock is not None:
            return
        s = socket.socket(socket.AF_UNIX, socket.SOCK_STREAM)
        # Retry briefly: the binary's listener may take a few ms to bind.
        deadline = time.monotonic() + 5.0
        last_err = None
        while time.monotonic() < deadline:
            try:
                s.connect(self._path)
                self._sock = s
                return
            except (FileNotFoundError, ConnectionRefusedError) as e:
                last_err = e
                time.sleep(0.05)
        raise RuntimeError(f"aux IPC connect failed: {last_err}")

    def _recv_full(self, n: int) -> bytes:
        buf = bytearray()
        while len(buf) < n:
            chunk = self._sock.recv(n - len(buf))
            if not chunk:
                raise RuntimeError("aux IPC: peer closed during recv")
            buf.extend(chunk)
        return bytes(buf)

    def _read_ack(self, req_id: int) -> None:
        ack = self._recv_full(_AUX_ACK.size)
        magic, status, ack_req = _AUX_ACK.unpack(ack)
        if magic != _AUX_ACK_MAGIC:
            raise RuntimeError(f"aux IPC: bad ack magic 0x{magic:08x}")
        if ack_req != req_id:
            raise RuntimeError(
                f"aux IPC: ack req_id mismatch ({ack_req} != {req_id})")
        if status != 0:
            raise RuntimeError(f"aux IPC: command failed (status={status})")


