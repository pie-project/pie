"""Worker entry for the ggml driver.

Lifecycle:
  * Resolves the `pie_driver_ggml` binary, the HF snapshot dir, and writes a
    startup TOML the binary consumes.
  * Spawns the binary and waits for its `READY {json}` line on stdout.
  * Reports the resulting `DriverCapabilities` to the engine via `ready_queue`.
  * Serves the control-plane RPC channel (ping / page-swap / adapter ops). The
    fast path (`fire_batch`) is served directly by the binary on `/pie_shmem`;
    pie_runtime is the client.

The binary loads HF safetensors directly (no GGUF). Page allocation is
owned by Pie's runtime — the binary honors whatever page IDs arrive in
each BPIQ request.
"""

from __future__ import annotations

import json
import os
import selectors
import shutil
import signal
import socket
import struct
import subprocess
import threading
import time
import traceback
from pathlib import Path

import msgpack

from pie._runtime import RpcServer
