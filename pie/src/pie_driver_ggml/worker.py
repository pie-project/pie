# RPC dispatch (control plane only)
# =============================================================================


def _make_methods(aux: "_AuxClient | None"):
    """Returns the cold-path method table. The fast path (`fire_batch`) is
    served by the binary directly on `/pie_shmem`; we never see it here.

    `aux` is the connection to the binary's aux IPC server when M7 page
    swap is enabled. None → page-copy RPCs return success as a no-op (this
    is safe for inferlets that don't actually depend on the data being
    copied; the runtime tolerates `swap_pool_size = 0` in capabilities)."""

    def _ping(**_):
        return {"ok": True}

    def _query(**_):
        return {"driver": "ggml", "implemented": False}

    def _unimplemented(name, milestone):
        def stub(**_):
            raise RuntimeError(
                f"{name!r} is not yet implemented in ggml driver "
                f"(planned for {milestone})"
            )
        return stub

    def _noop_ok(**_):
        return {"ok": True}

    # Method ID constants — must match `driver/ggml/src/aux_ipc.hpp`.
    METHOD_D2H, METHOD_H2D, METHOD_D2D, METHOD_H2H = 1, 2, 3, 4

    def _load_adapter_handler(client: "_AuxClient | None"):
        """Wrapper-side load_adapter: write the LoRA bytes to a temp file
        and forward the path to the binary's adapter loader."""
        def handler(**kwargs):
            if client is None:
                raise RuntimeError(
                    "load_adapter requires the aux IPC channel — "
                    "ensure the binary supports M7 wiring")
            adapter_ptr = int(kwargs["adapter_ptr"])
            data = kwargs.get("adapter_data") or kwargs.get("data") or b""
            if isinstance(data, list):
                data = bytes(data)
            tmp_dir = Path(os.environ.get("PIE_HOME", Path.home() / ".pie"))
            tmp_dir.mkdir(parents=True, exist_ok=True)
            tmp_path = tmp_dir / f"ggml-lora-{adapter_ptr}.safetensors"
            tmp_path.write_bytes(bytes(data))
            client.load_adapter(adapter_ptr, str(tmp_path))
            return {"ok": True, "adapter_ptr": adapter_ptr}
        return handler

    def _copy_via_aux(method: int, src_key: str, dst_key: str):
        """Build a handler that translates kwargs → aux IPC command."""
        def handler(**kwargs):
            if aux is None:
                # No aux IPC — accept silently (degraded mode).
                return {"ok": True}
            srcs = list(kwargs[src_key])
            dsts = list(kwargs[dst_key])
            if len(srcs) != len(dsts):
                raise ValueError(
                    f"copy: src/dst length mismatch ({len(srcs)} vs {len(dsts)})")
            aux.call(method, list(zip(srcs, dsts)))
            return {"ok": True}
        return handler

    return {
        "ping": _ping,
        "query": _query,
        "fire_batch": _unimplemented("fire_batch", "shmem fast path"),  # defensive
        "embed_image": _unimplemented("embed_image", "post-v1"),
        "initialize_adapter": _unimplemented("initialize_adapter",
                                              "post-v1 (CMAES)"),
        "update_adapter": _unimplemented("update_adapter",
                                          "post-v1 (CMAES)"),
        "save_adapter": _unimplemented("save_adapter", "post-v1"),
        "load_adapter": _load_adapter_handler(aux),
        # Page-copy / swap RPCs. When the binary's aux IPC server is up
        # (cpu_pages > 0), forward each pair-list call to it; the binary
        # executes the actual ggml_backend_tensor_get/set per page per
        # layer. swap_out/in_pages share the d2h/h2d wire — the runtime
        # uses them interchangeably in current versions.
        "copy_d2h":       _copy_via_aux(METHOD_D2H, "phys_ids",     "slots"),
        "copy_h2d":       _copy_via_aux(METHOD_H2D, "slots",        "phys_ids"),
        "copy_d2d":       _copy_via_aux(METHOD_D2D, "src_phys_ids", "dst_phys_ids"),
        "copy_h2h":       _copy_via_aux(METHOD_H2H, "src_slots",    "dst_slots"),
        "swap_out_pages": _copy_via_aux(METHOD_D2H, "phys_ids",     "slots"),
        "swap_in_pages":  _copy_via_aux(METHOD_H2D, "slots",        "phys_ids"),
    }


def _run_rpc_loop(
    server: RpcServer,
    stop_event: threading.Event,
    methods: dict,
) -> None:
    """Mirror of pie_driver's leader loop on the cold path."""
    SPIN_US = 10_000
    POLL_TIMEOUT_MS = 200

    while not stop_event.is_set():
        try:
            req = server.poll_busy(SPIN_US, POLL_TIMEOUT_MS)
        except RuntimeError as e:
            if stop_event.is_set() or "Interrupted" in str(e):
                break
            raise
        if req is None:
            continue
        request_id, method, payload, _send_walltime_us = req

        try:
            args = msgpack.unpackb(payload)
            fn = methods.get(method)
            if fn is None:
                response = msgpack.packb(f"Method not found: {method}")
                server.respond(request_id, response, 0)
                continue

            if isinstance(args, dict):
                result = fn(**args)
            elif isinstance(args, (list, tuple)):
                result = fn(*args)
            else:
                result = fn(args)

            response = msgpack.packb(result)
            server.respond(request_id, response, 0)
        except Exception as e:
            print(f"[ggml RPC Error] {method}: {e}\n{traceback.format_exc()}")
            response = msgpack.packb(str(e))
            server.respond(request_id, response, 0)


