from .config import GgmlDriverConfig


# =============================================================================
# Topology
# =============================================================================


def calculate_topology(world_size: int, tp_degree: int) -> list[list[int]]:
    """Server-driven helper: partition `world_size` ranks into TP groups.

    The ggml driver only supports tp_degree=1 in v1 (multi-rank lands
    post-v1). We still implement the helper so `pie/server.py` can call
    it uniformly and surface a clear error if the user asks for TP>1.
    """
    if world_size % tp_degree != 0:
        raise ValueError(
            f"world_size ({world_size}) must be divisible by tp_degree ({tp_degree})"
        )
    num_groups = world_size // tp_degree
    return [
        list(range(g * tp_degree, (g + 1) * tp_degree))
        for g in range(num_groups)
    ]


