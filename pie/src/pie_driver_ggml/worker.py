def worker_main(
    local_rank: int,
    world_size: int,
    devices: list[str],
    master_port: int,
    model_config: dict,
    driver_config: dict,
    group_topology: list[list[int]],
    ready_queue,
):
    """Entry point invoked via `mp.spawn` from `pie/server.py`."""
    if world_size != 1 or len(group_topology) != 1 or len(group_topology[0]) != 1:
        raise NotImplementedError(
            "ggml driver supports tensor_parallel_size=1 only "
            "(multi-rank is post-v1). Use the `native` driver for TP>1."
        )

    rank = local_rank

    driver_cfg = GgmlDriverConfig(**{
        k: v for k, v in driver_config.items()
        if k in GgmlDriverConfig.__dataclass_fields__
    })

    binary = _resolve_binary(driver_cfg.binary_path)

    # Reuse pie_driver's HF cache helper — same logic the Python `native`
    # driver uses to resolve `hf_repo` to a snapshot directory.
    from pie_driver.hf_utils import get_hf_snapshot_dir
    hf_repo = model_config["hf_repo"]
    snapshot_dir = get_hf_snapshot_dir(hf_repo)

    pie_home = Path(os.environ.get("PIE_HOME", Path.home() / ".pie"))
    pie_home.mkdir(parents=True, exist_ok=True)
    toml_path = pie_home / f"ggml-driver-{os.getpid()}.toml"
    aux_socket_path = str(pie_home / f"ggml-aux-{os.getpid()}.sock")
    _write_startup_toml(
        driver_cfg=driver_cfg,
        snapshot_dir=str(snapshot_dir),
        aux_socket_path=aux_socket_path,
        out_path=toml_path,
    )

    # Cold-path RPC server first so the ready handshake can carry its name.
    server = RpcServer.create()
    server_name = server.server_name()

    proc = subprocess.Popen(
        [str(binary), "--config", str(toml_path)],
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        text=True,
        bufsize=1,  # line-buffered
    )

    try:
        caps_dict = _wait_for_ready(proc, driver_cfg.ready_timeout_s)
    except Exception:
        proc.kill()
        proc.wait(timeout=2)
        raise

    stderr_thread = threading.Thread(
        target=_stream_stderr, args=(proc,), daemon=True
    )
    stderr_thread.start()

    # Translate the binary's capability JSON into DriverCapabilities.
    caps = DriverCapabilities(
        total_pages=int(caps_dict.get("total_pages", driver_cfg.max_num_kv_pages)),
        kv_page_size=int(caps_dict.get("kv_page_size", driver_cfg.kv_page_size)),
        swap_pool_size=int(caps_dict.get("swap_pool_size", 0)),
        max_batch_tokens=int(caps_dict.get("max_batch_tokens", driver_cfg.max_batch_tokens)),
        max_batch_size=int(caps_dict.get("max_batch_size", driver_cfg.max_batch_size)),
        arch_name=str(caps_dict.get("arch_name", "")),
        vocab_size=int(caps_dict.get("vocab_size", 0)),
        max_model_len=int(caps_dict.get("max_model_len", 0)),
        activation_dtype=str(caps_dict.get("activation_dtype", "")),
        snapshot_dir=str(caps_dict.get("snapshot_dir") or snapshot_dir),
    )

    ready_queue.put((rank, server_name, caps))

    # Connect to the binary's aux IPC server. The binary creates the
    # socket on startup; we lazily connect on the first page-copy RPC.
    aux_client = _AuxClient(aux_socket_path)

    stop_event = threading.Event()
    methods = _make_methods(aux_client)

    def _shutdown(signum, frame):
        stop_event.set()

    signal.signal(signal.SIGTERM, _shutdown)
    signal.signal(signal.SIGINT, _shutdown)

    try:
        _run_rpc_loop(server, stop_event, methods)
    finally:
        try:
            aux_client.close()
        except Exception:
            pass
        if proc.poll() is None:
            proc.send_signal(signal.SIGTERM)
            try:
                proc.wait(timeout=driver_cfg.shutdown_timeout_s)
            except subprocess.TimeoutExpired:
                proc.kill()
                proc.wait(timeout=2)
        try:
            toml_path.unlink(missing_ok=True)
        except Exception:
            pass
