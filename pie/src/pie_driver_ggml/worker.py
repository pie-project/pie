# READY handshake
# =============================================================================


class _ReadyTimeout(RuntimeError):
    pass


def _wait_for_ready(proc: subprocess.Popen, timeout_s: float) -> dict:
    """Read lines from `proc.stdout` until a `READY {json}` arrives. Anything
    else is forwarded to stderr so the user sees binary log output during
    startup. Raises if the binary exits, times out, or emits malformed JSON."""
    if proc.stdout is None:
        raise RuntimeError("subprocess has no stdout pipe")

    sel = selectors.DefaultSelector()
    sel.register(proc.stdout, selectors.EVENT_READ)

    start = time.monotonic()

    while True:
        if proc.poll() is not None:
            raise RuntimeError(
                f"pie_driver_ggml exited before READY (rc={proc.returncode})"
            )

        elapsed = time.monotonic() - start
        if elapsed > timeout_s:
            raise _ReadyTimeout(f"no READY within {timeout_s:.1f}s")

        events = sel.select(timeout=min(0.5, timeout_s - elapsed))
        if not events:
            continue

        line = proc.stdout.readline()
        if not line:
            continue
        line = line.rstrip("\n")

        if line.startswith("READY "):
            return json.loads(line[len("READY "):])

        # Pre-READY chatter — pass through so users see startup progress.
        print(f"[pie_driver_ggml] {line}")


def _stream_stderr(proc: subprocess.Popen) -> None:
    """Relay the binary's stderr into our own stderr (best-effort)."""
    if proc.stderr is None:
        return
    for raw in iter(proc.stderr.readline, ""):
        if not raw:
            break
        print(f"[pie_driver_ggml] {raw.rstrip()}")


