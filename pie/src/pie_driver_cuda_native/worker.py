def _write_startup_toml(
    *,
    driver_cfg: CudaNativeDriverConfig,
    hf_repo: str,
    snapshot_dir: str,
    device: str,
    out_path: Path,
    group_id: int = 0,
) -> None:
    """Emit the binary's startup config. Format matches `driver/cuda/dev.toml`."""
    # Per-DP-replica shmem name, matching runtime/src/device.rs::shmem_name
    # (`/pie_shmem_g{idx}`). Hardcoding `/pie_shmem` collided silently when
    # multiple replicas shared a host.
    shmem_name = f"/pie_shmem_g{group_id}"
    lines = [
        "# Auto-generated by pie_driver_cuda_native; do not edit.",
        "",
        "[shmem]",
        f'name = "{shmem_name}"',
        "num_slots = 8",
        "req_buf = 4194304",
        # 8 MiB resp_buf matches what pie_driver/worker.py uses for native
        # — sized to fit a full-vocab Sampler::Dist payload (vocab × 8 bytes,
        # ≈2.6 MiB on 150K-vocab models) plus per-request overhead and the
        # spec-mode multi-slot tail. The Rust client reads this from the
        # shmem header at attach time, so changing it is purely a driver-side
        # decision.
        "resp_buf = 8388608",
        "spin_us = 0",
        "",
        "[model]",
        f'hf_repo = "{hf_repo}"',
        f'snapshot_dir = "{snapshot_dir}"',
        f'device = "{device}"',
        f'dtype = "{driver_cfg.weight_dtype}"',
        *([f'runtime_quant = "{driver_cfg.runtime_quant}"']
            if driver_cfg.runtime_quant else []),
        "",
        "[batching]",
        f"kv_page_size = {driver_cfg.kv_page_size}",
        f"max_batch_tokens = {driver_cfg.max_batch_tokens}",
        f"max_batch_size = {driver_cfg.max_batch_size}",
        f"max_num_kv_pages = {driver_cfg.max_num_kv_pages}",
        f"swap_pool_size = {driver_cfg.swap_pool_size}",
        "",
    ]
    out_path.write_text("\n".join(lines))


