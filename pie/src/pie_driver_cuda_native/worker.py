def _write_startup_toml(
    *,
    driver_cfg: CudaNativeDriverConfig,
    hf_repo: str,
    snapshot_dir: str,
    device: str,
    out_path: Path,
) -> None:
    """Emit the binary's startup config. Format matches `driver/cuda/dev.toml`."""
    # Minimal escaping — these strings are filesystem paths and HF repo IDs;
    # they don't contain double quotes in practice.
    lines = [
        "# Auto-generated by pie_driver_cuda_native; do not edit.",
        "",
        "[shmem]",
        'name = "/pie_shmem"',
        "num_slots = 8",
        "req_buf = 4194304",
        "resp_buf = 4194304",
        "spin_us = 0",
        "",
        "[model]",
        f'hf_repo = "{hf_repo}"',
        f'snapshot_dir = "{snapshot_dir}"',
        f'device = "{device}"',
        f'dtype = "{driver_cfg.weight_dtype}"',
        "",
        "[batching]",
        f"kv_page_size = {driver_cfg.kv_page_size}",
        f"max_batch_tokens = {driver_cfg.max_batch_tokens}",
        f"max_batch_size = {driver_cfg.max_batch_size}",
        f"max_num_kv_pages = {driver_cfg.max_num_kv_pages}",
        f"swap_pool_size = {driver_cfg.swap_pool_size}",
        "",
    ]
    out_path.write_text("\n".join(lines))


# =============================================================================
# READY handshake
# =============================================================================


class _ReadyTimeout(RuntimeError):
    pass


def _wait_for_ready(proc: subprocess.Popen, timeout_s: float) -> dict:
    """Read lines from `proc.stdout` until a `READY {json}` arrives. Anything
    else is forwarded to stderr so the user sees binary log output during
    startup. Raises if the binary exits, times out, or emits malformed JSON."""
    import selectors

    if proc.stdout is None:
        raise RuntimeError("subprocess has no stdout pipe")

    sel = selectors.DefaultSelector()
    sel.register(proc.stdout, selectors.EVENT_READ)

    deadline_remaining = timeout_s
    import time
    start = time.monotonic()

    while True:
        if proc.poll() is not None:
            raise RuntimeError(
                f"pie_driver_cuda exited before READY (rc={proc.returncode})"
            )

        elapsed = time.monotonic() - start
        if elapsed > timeout_s:
            raise _ReadyTimeout(f"no READY within {timeout_s:.1f}s")

        events = sel.select(timeout=min(0.5, timeout_s - elapsed))
        if not events:
            continue

        line = proc.stdout.readline()
        if not line:
            continue
        line = line.rstrip("\n")

        if line.startswith("READY "):
            return json.loads(line[len("READY "):])

        # Pass through pre-READY chatter so users see startup progress.
        print(f"[pie_driver_cuda] {line}")


def _stream_stderr(proc: subprocess.Popen) -> None:
    """Relay the binary's stderr into our own stderr (best-effort)."""
    if proc.stderr is None:
        return
    for raw in iter(proc.stderr.readline, ""):
        if not raw:
            break
        print(f"[pie_driver_cuda] {raw.rstrip()}")


# =============================================================================
# RPC dispatch (control plane only)
# =============================================================================


class _CtrlClient:
    """Wrapper-side client for the C++ binary's control socket.

    Sends `copy_d2h / copy_h2d / copy_d2d / copy_h2h` over a `SOCK_SEQPACKET`
    socketpair. Wire format and method tags live in
    `control_protocol.py` (kept in sync with `control_socket.hpp`).
    """

    # Method tags re-exported on the class for backwards-compat with
    # call sites that already use `_CtrlClient.METHOD_COPY_*`.
    METHOD_COPY_D2H = _proto.METHOD_COPY_D2H
    METHOD_COPY_H2D = _proto.METHOD_COPY_H2D
    METHOD_COPY_D2D = _proto.METHOD_COPY_D2D
    METHOD_COPY_H2H = _proto.METHOD_COPY_H2H

    def __init__(self, sock: socket.socket):
        self._sock = sock
        self._lock = threading.Lock()

    def call(self, method: int, layer: int,
             srcs: list[int], dsts: list[int]) -> int:
        if len(srcs) != len(dsts):
            raise ValueError(f"src/dst length mismatch: {len(srcs)} vs {len(dsts)}")
        n = len(srcs)
        msg = (_proto.HEADER.pack(method, layer, n, 0)
               + struct.pack(f"<{n}I", *srcs)
               + struct.pack(f"<{n}I", *dsts))
        with self._lock:
            self._sock.send(msg)
            resp = self._sock.recv(_proto.RESPONSE_SIZE)
        if len(resp) != _proto.RESPONSE_SIZE:
            raise RuntimeError(f"control socket truncated response ({len(resp)} bytes)")
        return struct.unpack("<I", resp)[0]


def _make_methods(ctrl: "_CtrlClient | None" = None):
    """Returns the cold-path method table. The fast path (`fire_batch`) is
    served by the binary; we never see it on this channel."""

    def _ping(**_):
        return {"ok": True}

    def _query(**_):
        # `pie_driver` exposes lightweight engine introspection here; for now
        # we return a placeholder until M2 wires queries through to the binary.
        return {"driver": "cuda_native", "implemented": False}

    def _unimplemented(name):
        def stub(**_):
            raise RuntimeError(
                f"{name!r} is not yet implemented in cuda_native (planned for M6+)"
            )
        return stub

    def _require_ctrl(name):
        if ctrl is None:
            raise RuntimeError(
                f"{name!r} called but cuda_native was started without a "
                "control socket — set swap_pool_size > 0 and re-spawn the "
                "driver to enable KV-page swap."
            )

    # Pie's runtime passes argument names that mirror `pie_driver`:
    #   swap_out / swap_in / copy_d2h / copy_h2d → phys_ids, slots
    #   copy_d2d                                → src_phys_ids, dst_phys_ids
    #   copy_h2h                                → src_slots, dst_slots
    # We don't pass a layer index — the binary applies copies across every
    # layer (matches Python's per-layer index_copy_ loop).
    def _swap_out(**kw):
        _require_ctrl("copy_d2h")
        srcs, dsts = list(kw["phys_ids"]), list(kw["slots"])
        s = ctrl.call(_CtrlClient.METHOD_COPY_D2H, 0, srcs, dsts)
        if s != 0: raise RuntimeError(f"copy_d2h failed: status={s}")
        return {"ok": True}

    def _swap_in(**kw):
        _require_ctrl("copy_h2d")
        # pie_driver's H2D contract: `phys_ids` are GPU destinations,
        # `slots` are CPU sources. Wire format is (src, dst) — flip.
        srcs, dsts = list(kw["slots"]), list(kw["phys_ids"])
        s = ctrl.call(_CtrlClient.METHOD_COPY_H2D, 0, srcs, dsts)
        if s != 0: raise RuntimeError(f"copy_h2d failed: status={s}")
        return {"ok": True}

    def _copy_d2d(**kw):
        _require_ctrl("copy_d2d")
        srcs = list(kw["src_phys_ids"])
        dsts = list(kw["dst_phys_ids"])
        s = ctrl.call(_CtrlClient.METHOD_COPY_D2D, 0, srcs, dsts)
        if s != 0: raise RuntimeError(f"copy_d2d failed: status={s}")
        return {"ok": True}

    def _copy_h2h(**kw):
        _require_ctrl("copy_h2h")
        srcs = list(kw.get("src_slots", kw.get("src_phys_ids", [])))
        dsts = list(kw.get("dst_slots", kw.get("dst_phys_ids", [])))
        s = ctrl.call(_CtrlClient.METHOD_COPY_H2H, 0, srcs, dsts)
        if s != 0: raise RuntimeError(f"copy_h2h failed: status={s}")
        return {"ok": True}

    return {
        "ping": _ping,
        "query": _query,
        "fire_batch": _unimplemented("fire_batch"),  # never hit; defensive
        "embed_image": _unimplemented("embed_image"),
        "initialize_adapter": _unimplemented("initialize_adapter"),
        "update_adapter": _unimplemented("update_adapter"),
        "load_adapter": _unimplemented("load_adapter"),
        "save_adapter": _unimplemented("save_adapter"),
        "swap_out_pages": _swap_out,
        "swap_in_pages":  _swap_in,
        "copy_d2h": _swap_out,
        "copy_h2d": _swap_in,
        "copy_d2d": _copy_d2d,
        "copy_h2h": _copy_h2h,
    }


def _run_rpc_loop(
    server: RpcServer,
    stop_event: threading.Event,
    methods: dict,
) -> None:
    """Mirror of pie_driver's leader loop on the cold path."""
    SPIN_US = 10_000
    POLL_TIMEOUT_MS = 200

    while not stop_event.is_set():
        try:
            req = server.poll_busy(SPIN_US, POLL_TIMEOUT_MS)
        except RuntimeError as e:
            # SIGTERM/SIGINT interrupts the underlying recv() with EINTR.
            # If shutdown was requested, treat as graceful exit; otherwise
            # re-raise so genuine IPC errors aren't swallowed.
            if stop_event.is_set() or "Interrupted" in str(e):
                break
            raise
        if req is None:
            continue
        request_id, method, payload, _send_walltime_us = req

        try:
            args = msgpack.unpackb(payload)
            fn = methods.get(method)
            if fn is None:
                response = msgpack.packb(f"Method not found: {method}")
                server.respond(request_id, response, 0)
                continue

            if isinstance(args, dict):
                result = fn(**args)
            elif isinstance(args, (list, tuple)):
                result = fn(*args)
            else:
                result = fn(args)

            response = msgpack.packb(result)
            server.respond(request_id, response, 0)
        except Exception as e:
            import traceback
            print(f"[cuda_native RPC Error] {method}: {e}\n{traceback.format_exc()}")
            response = msgpack.packb(str(e))
            server.respond(request_id, response, 0)


