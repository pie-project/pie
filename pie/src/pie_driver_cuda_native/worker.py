def _resolve_binary(override: str) -> Path:
    """Find `pie_driver_cuda`. Checks override, env var, $PATH, then a sibling
    build tree. Raises with a useful hint if nothing works."""
    if override:
        p = Path(override).expanduser()
        if p.is_file() and os.access(p, os.X_OK):
            return p
        raise FileNotFoundError(f"binary_path={override!r} is not an executable file")

    if env := os.environ.get("PIE_CUDA_DRIVER_BIN"):
        p = Path(env).expanduser()
        if p.is_file() and os.access(p, os.X_OK):
            return p

    if which := shutil.which("pie_driver_cuda"):
        return Path(which)

    # Fall back to a relative search: this file lives at
    # <repo>/pie/src/pie_driver_cuda_native/worker.py, so the build tree is
    # at <repo>/driver/cuda/build/bin/pie_driver_cuda.
    candidate = Path(__file__).resolve().parents[3] / "driver" / "cuda" / "build" / "bin" / "pie_driver_cuda"
    if candidate.is_file() and os.access(candidate, os.X_OK):
        return candidate

    raise FileNotFoundError(
        "pie_driver_cuda binary not found. Build it via\n"
        "    cd driver/cuda && cmake -S . -B build -G Ninja && cmake --build build\n"
        "or set $PIE_CUDA_DRIVER_BIN to its path."
    )


